package com.example.cashmanager.Model;

public class Data {
    String name, username, pass, repass, img, key, date;
    private String amount;
    private String type;
    private String note;
    private String id;

    public Data() {
    }

    public Data(String name, String username, String pass, String repass) {
        this.name = name;
        this.username = username;
        this.pass = pass;
        this.repass = repass;
    }

    public Data(String img) {
        this.img = img;
    }

    public Data(String amount, String type, String note, String date, String id) {

        this.amount = amount;
        this.type = type;
        this.note = note;
        this.date = date;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getRepass() {
        return repass;
    }

    public void setRepass(String repass) {
        this.repass = repass;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
